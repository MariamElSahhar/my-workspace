#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		count;
	t_list	*ptr;

	count = 0;
	ptr = lst;
	while (ptr->next != 0)
	{
		ptr = ptr->next;
		count++;
	}
	return (count);
}
